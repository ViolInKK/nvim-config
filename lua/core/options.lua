local opt = vim.opt

-- line number
opt.number = true

-- tabs & indentation
opt.tabstop = 4
opt.shiftwidth = 4
opt.autoindent = true
opt.expandtab = true

-- line wrapping
opt.wrap = true
opt.linebreak = true
 
-- search settings
opt.ignorecase = true
opt.smartcase = true

-- split windows
opt.splitbelow = true 

-- clipboard
opt.clipboard= "unnamed"

-- spelling
opt.spelllang = { "en_us" }
opt.spell = true

